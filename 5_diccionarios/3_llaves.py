diccionario = {'a': 1, 'b': 2, 'c': 3, 'd': 4,}

# keys obtiene todas las llaves del diccionario
llaves  = diccionario.keys()
print(llaves)

# values obtiene todas los valores del diccionario
valores  = diccionario.values()
print(valores)

# items obtiene todos los items del diccionario
elementos  = diccionario.items()
print(elementos)